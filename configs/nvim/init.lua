          return {
            timeout_ms = 500,
            lsp_format = 'fallback',
          }
        end
      end,
      formatters_by_ft = {
        lua = { 'stylua' },
        latex = { 'latexindent' },
        r = { 'r_language_server' },
        rust = { 'rust_analyzer' },
        toml = { 'taplo' },
